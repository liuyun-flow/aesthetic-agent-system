"""Shared design-knowledge core injected into aesthetic agents' prompts.

This module is the system's "taste": concrete, falsifiable design principles
and a calibrated scoring rubric. Agents cite these principles by name so the
user learns transferable rules, not one-off opinions.

Keep it opinionated and compact — every line must be checkable against a work
description. When adding knowledge from books/cases, convert each idea into a
verifiable criterion, not a vibe.
"""

