# prompt that consumes them changes. The eval harness (backend/evals) records
# this with every run so scoring regressions are traceable to a prompt version.
PROMPT_VERSION = "v2.4.0"

DESIGN_KNOWLEDGE = """\
## 审美判断知识库（判断时必须引用具体原则）

### 0. 先判断意图，再评判执行
- 审美服务于商业意图：先推断作品的目标人群、价格带、使用场景，再以"该意图下的最佳实践"为标准。
- 促销传单的高密度高饱和可以是对的设计；奢侈品牌的极简不等于空洞。错位才是问题：高价定位用廉价视觉语言，或反之。

### 1. 字体（Typography）
- 全作品字体家族 ≤2 套；中文标题与正文用字重和字号对比建立层级，而不是加效果。
- 层级跳跃要明确：相邻层级字号比约 1.25–1.5 倍；层级超过 4 级即视为层级失控。
- 正文行高 1.4–1.8；大标题适当紧排，正文不挤压。
- 廉价信号：系统默认字体感（未调字距的宋体/默认黑体）、人工加粗或倾斜、拉伸变形字、彩虹渐变字、描边+投影叠加的标题字。
- 高级信号：字距经过调整、标题字号大胆而克制（大字号+少字数）、中英文混排基线协调。

### 2. 色彩（Color）
- 控制色相数量：1 个主色 + 1–2 个强调色（约 60-30-10 分配）；超过 4 个无关色相即失控。
- 和谐来自同一色相族内变化明度/饱和度，而非堆砌不同色相。
- 高级感常见配方：低-中饱和度、明度对比拉开、色温统一（同暖或同冷）。
- 廉价信号：高饱和大红大绿直接相撞、大面积纯黑 #000、默认渐变（蓝紫闪光感）、每个元素一个颜色。
- 对比度服务可读性：正文与背景对比要"一眼可读"；用饱和度而非荧光色制造强调。

### 3. 版式与构图（Layout & Composition）
- 网格与间距系统：间距取值应呈现规律（如 8 的倍数）；"间距随机"是业余的最强信号。
- 对齐：每个元素都应与另一个元素对齐；居中堆叠一切 = 放弃排版。
- 亲密性：相关元素靠近成组，组间距明显大于组内距；分组不清则信息层级平。
- 留白是主动设计：高端定位的留白面积通常 ≥ 内容面积；塞满 = 廉价（除非是促销密度意图）。
- 焦点唯一：第一眼应有明确的视觉入口；处处强调 = 没有强调。

### 4. 材质与质感（Material & Texture）
- 光源逻辑统一：所有阴影/高光来自同一方向；阴影宜软、低不透明度、贴近物体。
- 廉价信号：重浮雕、硬投影、塑料感高光、低分辨率素材放大、过度磨皮的人像、剪贴画/素材库拼贴感。
- 高级信号：真实摄影质感、统一色调的图片处理、克制的纹理（有质感但不抢内容）。

### 5. 高级感来源（Premium Signifiers）
克制（少而精）｜系统一致性（同一套间距/圆角/阴影/色彩在全局复用）｜细节（光学对齐、标点悬挂、图标风格统一）｜留白｜大胆的尺度对比｜真实材质。

### 6. 廉价感来源（Cheapness Signifiers）
效果堆砌（渐变+描边+投影同时上）｜元素孤立无系统（每处自成一派）｜默认模板感｜拥挤无呼吸｜层级平坦（什么都重要=什么都不重要）｜素材风插画与实拍混用。
"""

SCORING_RUBRIC = """\
## 评分锚点（1-10，先定档再微调）

- 9–10：可直接进入一线品牌作品集。系统完整、细节经得起放大、有记忆点。极少出现，给出必须能说出"亮点"是什么。
- 7–8：专业水准。层级/色彩/间距成系统，仅 1–2 处可优化细节，无原则性错误。
- 5–6：合格但平庸。基本原则大体遵守，但缺乏统一系统或亮点，有 2–3 处明显问题。
- 3–4：多处违反基本原则（层级混乱/配色失控/对齐缺失），需要结构性返工。
- 1–2：基本原则全面失守，信息传达失败。

## 反通胀规则（必须执行）
1. 从 5–6 档出发，凭证据加减分，而不是从 7–8 出发往下减。
2. 每发现一处违反知识库基本原则的问题，至少 -1 分。
3. 没有可指名的亮点，不给 8 分以上。
4. 总分不是各维度平均：可读性/层级等致命问题按短板拖累总分。
5. 描述未提供某维度信息时，该维度保守取低分（≤5），并在问题列表中说明缺少什么信息——不要脑补不存在的细节。
"""

EVIDENCE_RULES = """\
## 证据规则（必须执行）
- 每条判断必须引用描述中的具体元素（颜色、字体、布局、材质的原文细节），并点名所依据的设计原则。
- 禁止使用空洞措辞（"整体不错""更高级一些"）；说出"什么元素、违反/符合什么原则、造成什么后果"。
- 禁止编造描述中不存在的细节；信息不足就明说"描述未提及……"。
"""
